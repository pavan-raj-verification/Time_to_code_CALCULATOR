/**
 * Calculates the Addition of two numbers.
 * Formula: Result = a+b
 * @param[in] a,b
 * @param[out] a+b
 */
int add(int a,int b)
{
    return a+b;
}
/**
 * Calculates the Subtraction of two numbers.
 * Formula: Result = a-b
 * @param[in] a,b
 * @param[out] a-b
 */
int sub(int a,int b)
{

    return a-b;

}
